## \namespace reduction::applications::Lrmecs
##
## This package continas powder reduction applications for Lrmecs instrument
##
## - Reduction applications
##   - LrmecsReduction: Powder reduction application with the full
##     configurability
##   - LrmecsReductionLight: Light version of LrmecsRedcution with
##     easier user settings.
##   - wxLrmecsReductionLight: gui interface for LrmecsReductionLight
##
## - Helper applications
##   - LrmecsPlotI_tof: Plot I(tof) for a Lrmecs dataset to help find
##     out time-independent background
##   - LrmecsVanPlotI_phi: Plot I(phi) for a Lrmecs vanadium run dataset
##     to help find out dark-angle
##   - LrmecsPlotMonI_tof: Plot I(tof) for monitor data in a Lrmecs dataset
##     to help find out the tof region in which neutrons hit the monitor
##   - lrmecs2ascii: perl script to convert LRMECS data file in binary format
##     to a special text format recognized by LRMECS reduction applications
##     in this package.
##
## A tutorial of the wxLrmecsReductionLight application is available
## <a href="http://wiki.cacr.caltech.edu/danse/index.php/LrmecsReductionLight">here</a>
