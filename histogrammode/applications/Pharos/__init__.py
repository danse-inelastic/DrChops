## \namespace reduction::applications::Pharos
##
## This package continas reduction applications for Pharos instrument
##
## Currently only powder reduction applications are available.
##
## - Reduction applications
##   - PharosReduction_v2: Powder reduction application with the full
##     configurability
##   - PharosReduction_v2_Parallel: parallel version of PharosReduction_v2.py
##   - PharosReductionLight: Light version of PharosRedcution_v2.py with
##     easier user settings.
##   - wxPharosReductionLight: gui interface for PharosReductionLight.py
##
## - Helper applications
##   - PharosPlotI_tof: Plot I(tof) for a Pharos dataset to help find
##     out time-independent background
##   - PharosVanPlotI_phi: Plot I(phi) for a Pharos vanadium run dataset
##     to help find out dark-angle
##
## A tutorial of the wxPharosReductionLight.py application is available
## <a href="http://wiki.cacr.caltech.edu/danse/index.php/PharosReductionLight">here</a>
